package session_transcript

import (
	"bytes"
	"testing"
)

func TestAppleHandoverV1(t *testing.T) {
	merchantID := "merchant123"
	temaID := "tema123"
	nonce := []byte("testnonce")
	requesterIdHash := []byte("requesterIdHash")

	expectedOutput := []byte{
		0x83, 0xf6, 0xf6, 0x85, 0x78, 0x1c, 0x41, 0x70, 0x70, 0x6c,
		0x65, 0x49, 0x64, 0x65, 0x6e, 0x74, 0x69, 0x74, 0x79, 0x50,
		0x72, 0x65, 0x73, 0x65, 0x6e, 0x74, 0x6d, 0x65, 0x6e, 0x74,
		0x5f, 0x31, 0x2e, 0x30, 0x49, 0x74, 0x65, 0x73, 0x74, 0x6e,
		0x6f, 0x6e, 0x63, 0x65, 0x6b, 0x6d, 0x65, 0x72, 0x63, 0x68,
		0x61, 0x6e, 0x74, 0x31, 0x32, 0x33, 0x67, 0x74, 0x65, 0x6d,
		0x61, 0x31, 0x32, 0x33, 0x4f, 0x72, 0x65, 0x71, 0x75, 0x65,
		0x73, 0x74, 0x65, 0x72, 0x49, 0x64, 0x48, 0x61, 0x73, 0x68,
	} // Correct expected CBOR encoded output

	transcript, err := AppleHandoverV1(merchantID, temaID, nonce, requesterIdHash)
	if err != nil {
		t.Fatalf("unexpected error: %v", err)
	}
	if !bytes.Equal(transcript, expectedOutput) {
		t.Errorf("expected %v, got %v", expectedOutput, transcript)
	}
}
